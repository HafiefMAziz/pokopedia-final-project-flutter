class User {
  final int id;
  final String username;
  final String email;
  final String password;
  final String fullname;
  final String avatar;
  final String address;

  User({
    required this.id,
    required this.username,
    required this.email,
    required this.password,
    required this.fullname,
    required this.avatar,
    required this.address,
  });
}
